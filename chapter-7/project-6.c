#include <stdio.h>

int main() {

    printf("Size of int type: %d bytes\n", sizeof(int));
    printf("Size of short type: %d bytes\n", sizeof(short));
    printf("Size of long type: %d bytes\n", sizeof(long));
    printf("Size of float type: %d bytes\n", sizeof(float));
    printf("Size of double type: %d bytes\n", sizeof(double));
    printf("Size of long double type: %d bytes\n", sizeof(long double));

    return 0;
}